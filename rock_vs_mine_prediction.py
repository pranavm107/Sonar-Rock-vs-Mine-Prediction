# group this data based on mine and rock
sonar_data.groupby(60).mean()

#seprating data and label
X = sonar_data.drop(columns=60, axis=1) #storing all values expect 60th column
y = sonar_data[60] # stoing 60th column in y

print(X)
print(y)

"""Training and Testing"""

X_train, X_test, y_train, y_test = train_test_split(
    X, y, test_size = 0.1, stratify=y, random_state=1
)

print(X.shape, X_train.shape, X_test.shape)

"""Model Trainig ----> Logistic Regression"""

model = LogisticRegression()

#training the logistic regression model with training data
model.fit(X_train, y_train)

"""Model Evaluation"""

# to find accuracy of our model on training data
X_train_prediction = model.predict(X_train)
training_data_accuracy = accuracy_score(X_train_prediction, y_train)

print("Accuracy on training data: ", training_data_accuracy)

X_test_prediction = model.predict(X_test)
test_data_accuracy = accuracy_score(X_test_prediction, y_test)

print("Accuracy on test data: ", test_data_accuracy)

